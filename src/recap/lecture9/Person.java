package recap.lecture9;

public class Person {
    String name; // fields - oni nazyvajutsja poljami
    int age; // fields - oni nazyvajutsja poljami


    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
